class JourneyInfo {
  final double distance;
  final double travelHours;

  final double caloriesRequired;
  final double waterRequired;
  final double forageRequired;
  final double fuelRequired;

  const JourneyInfo({
    required this.distance,
    required this.travelHours,
    required this.caloriesRequired,
    required this.waterRequired,
    required this.forageRequired,
    required this.fuelRequired,
  });
}